package io.fletchly.comparator.core.port.out

import io.fletchly.comparator.core.model.message.Message
import io.fletchly.comparator.core.model.user.User

/**
 * Displays chat messages to users
 */
interface ChatPort {
    /**
     * Sends a user-generated message to the chat system for display.
     *
     * @param target The user for whom to display the message.
     * @param message The message sent by the user, containing its content and sender details.
     */
    suspend fun user(target: User, message: Message.User)

    /**
     * Sends an assistant-generated message to the chat system for display.
     *
     * @param target The user for whom to display the message.
     * @param message The message generated by the assistant.
     */
    suspend fun assistant(target: User, message: Message.Assistant)
}